def reciprocal_rank_fusion(results_list: list[list[SearchResult]], k: int = 60,
                           top_k: int = HYBRID_TOP_K) -> list[SearchResult]:
    """Merge ranked lists using RRF: score(d) = Σ 1/(k + rank)."""
    rrf_scores = {}
    for result_list in results_list:
        for rank, result in enumerate(result_list):
            if result.text not in rrf_scores: 
                rrf_scores[result.text] = {"score": 0.0, "result": result}
            rrf_scores[result.text]["score"] += 1.0 / (k + rank + 1)
            
    sorted_items = sorted(rrf_scores.values(), key=lambda x: x["score"], reverse=True)
    top_items = sorted_items[:top_k]
    
    final_results = []
    for item in top_items:
        res = item["result"]
        final_results.append(SearchResult(text=res.text, score=item["score"], metadata=res.metadata, method="hybrid"))
        
    return final_results


class HybridSearch:
    """Combines BM25 + Dense + RRF. (Đã implement sẵn — dùng classes ở trên)"""
    def __init__(self):
        self.bm25 = BM25Search()
        self.dense = DenseSearch()

    def index(self, chunks: list[dict]):
        print("  Starting BM25.index...", flush=True)
        self.bm25.index(chunks)
        print("  Finished BM25.index. Starting Dense.index...", flush=True)
        self.dense.index(chunks)
        print("  Finished Dense.index.", flush=True)

    def search(self, query: str, top_k: int = HYBRID_TOP_K) -> list[SearchResult]:
        bm25_results = self.bm25.search(query, top_k=BM25_TOP_K)
        dense_results = self.dense.search(query, top_k=DENSE_TOP_K)
        return reciprocal_rank_fusion([bm25_results, dense_results], top_k=top_k)


